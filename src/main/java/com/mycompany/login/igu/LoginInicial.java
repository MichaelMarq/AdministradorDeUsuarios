    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String contra = txtContra.getText();
        
        Usuario validar = control.validarUsuario(usuario,contra);
        
        if(validar != null){
            
            if(validar.getUnRol().getNombreRol().equals("admin")){
                PrincipalAdmin ventana = new PrincipalAdmin(control,validar);
                ventana.setVisible(true);
                ventana.setLocationRelativeTo(null);
                dispose();
            }
            else if(validar.getUnRol().getNombreRol().equals("user")){
                PrincipalUser ventana = new PrincipalUser(control,validar);
                ventana.setVisible(true);
                ventana.setLocationRelativeTo(null);
                dispose();
            }
        }
        else{
            txtArea.setText("Usuario o contraseña incorrectos");
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed



